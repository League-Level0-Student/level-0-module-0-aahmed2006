package strings_and_dialogs;
import javax.swing.JOptionPane;

public class KnockKnock {
public static void main(String[] args) {
	JOptionPane.showMessageDialog(null, "Knock knock.");
	JOptionPane.showMessageDialog(null, "Who's there?");
	JOptionPane.showMessageDialog(null, "Voodoo.");
	JOptionPane.showMessageDialog(null, "Voodoo who?");
	JOptionPane.showMessageDialog(null, "Voodoo you think you are, asking me so many questions?");
}
}
